-- 신규 Auth 사용자의 metadata를 profiles로 복사
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = public
as $$
begin
  insert into public.profiles(id, emp_no, name, department, team, position)
  values(
    new.id,
    upper(coalesce(new.raw_user_meta_data->>'emp_no', split_part(new.email,'@',1))),
    coalesce(new.raw_user_meta_data->>'name', split_part(new.email,'@',1)),
    new.raw_user_meta_data->>'department',
    new.raw_user_meta_data->>'team',
    new.raw_user_meta_data->>'position'
  )
  on conflict (id) do nothing;

  insert into public.user_permissions(user_id) values(new.id)
  on conflict (user_id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute procedure public.handle_new_user();

create or replace function public.is_super_admin()
returns boolean language sql stable security definer set search_path=public
as $$ select coalesce((select is_super_admin from public.profiles where id=auth.uid()),false); $$;

create or replace function public.has_permission(permission_name text)
returns boolean language plpgsql stable security definer set search_path=public
as $$
declare result boolean;
begin
  if public.is_super_admin() then return true; end if;
  execute format('select coalesce(%I,false) from public.user_permissions where user_id=$1',permission_name)
  into result using auth.uid();
  return coalesce(result,false);
end;
$$;

