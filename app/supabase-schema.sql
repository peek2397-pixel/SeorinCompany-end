create policy "profiles readable by organization users" on public.profiles for select to authenticated using (public.has_permission('organization_view') or id=auth.uid());
create policy "profiles managed by admin" on public.profiles for all to authenticated using (public.has_permission('employees_manage')) with check (public.has_permission('employees_manage'));

create policy "own permission readable" on public.user_permissions for select to authenticated using (user_id=auth.uid() or public.has_permission('permissions_manage'));
create policy "permissions managed by admin" on public.user_permissions for all to authenticated using (public.has_permission('permissions_manage')) with check (public.has_permission('permissions_manage'));

create policy "notices readable" on public.notices for select to authenticated using (public.has_permission('notices_view'));
create policy "notices writable" on public.notices for all to authenticated using (public.has_permission('notices_manage')) with check (public.has_permission('notices_manage'));

create policy "private messages participant read" on public.private_messages for select to authenticated
using (sender_id=auth.uid() or recipient_id=auth.uid() or public.is_super_admin());
create policy "private messages sender insert" on public.private_messages for insert to authenticated
with check (sender_id=auth.uid() and public.has_permission('private_messages_use'));
create policy "private messages recipient update" on public.private_messages for update to authenticated
using (recipient_id=auth.uid() or public.is_super_admin()) with check (recipient_id=auth.uid() or public.is_super_admin());

create policy "card own or manager read" on public.corporate_card_expenses for select to authenticated
using (created_by=auth.uid() or public.has_permission('card_manage'));
create policy "card insert" on public.corporate_card_expenses for insert to authenticated
with check (created_by=auth.uid() and public.has_permission('card_use'));
create policy "card manager update" on public.corporate_card_expenses for update to authenticated
using (public.has_permission('card_manage')) with check (public.has_permission('card_manage'));
create policy "card manager delete" on public.corporate_card_expenses for delete to authenticated
using (public.has_permission('card_manage'));

create policy "inventory readable" on public.inventory_items for select to authenticated using (public.has_permission('inventory_view'));
create policy "inventory managed" on public.inventory_items for all to authenticated
using (public.has_permission('inventory_manage')) with check (public.has_permission('inventory_manage'));
create policy "inventory tx readable" on public.inventory_transactions for select to authenticated using (public.has_permission('inventory_view'));
create policy "inventory tx insert" on public.inventory_transactions for insert to authenticated
with check (created_by=auth.uid() and public.has_permission('inventory_manage'));

create policy "purchase own or approver read" on public.purchase_requests for select to authenticated
using (requester_id=auth.uid() or public.has_permission('purchase_approve'));
create policy "purchase request insert" on public.purchase_requests for insert to authenticated
with check (requester_id=auth.uid() and public.has_permission('purchase_view'));
create policy "purchase approver update" on public.purchase_requests for update to authenticated
using (public.has_permission('purchase_approve')) with check (public.has_permission('purchase_approve'));

create policy "audit admin read" on public.audit_logs for select to authenticated using (public.is_super_admin());
create policy "audit authenticated insert" on public.audit_logs for insert to authenticated with check (user_id=auth.uid());

