begin;
create table if not exists public.freight_orders(
 id uuid primary key default gen_random_uuid(), ship_date date, arrival_date date, category text default '국내', customer text, address text, pallets numeric default 0, order_numbers text, notes text, status text default '등록', base_fare numeric default 0, waiting_fee numeric default 0, inspection_fee numeric default 0, loading_fee numeric default 0, stopover_fee numeric default 0, night_fee numeric default 0, other_fee numeric default 0, total_amount numeric default 0, ai_estimate numeric default 0, ai_basis text, created_by uuid references auth.users(id), created_by_name text, created_at timestamptz default now(), updated_at timestamptz default now()
);
alter table public.freight_orders enable row level security;
drop policy if exists "V126 freight select" on public.freight_orders;
create policy "V126 freight select" on public.freight_orders for select to authenticated using (
 public.is_super_admin() or public.has_permission('vehicles_view') or public.has_permission('vehicles_manage')
);
drop policy if exists "V126 freight insert" on public.freight_orders;
create policy "V126 freight insert" on public.freight_orders for insert to authenticated with check (
 auth.uid()=created_by and (public.is_super_admin() or public.has_permission('vehicles_view') or public.has_permission('vehicles_manage'))
);
drop policy if exists "V126 freight update" on public.freight_orders;
create policy "V126 freight update" on public.freight_orders for update to authenticated using (
 public.is_super_admin() or public.has_permission('vehicles_manage')
) with check (public.is_super_admin() or public.has_permission('vehicles_manage'));
drop policy if exists "V126 freight delete" on public.freight_orders;
create policy "V126 freight delete" on public.freight_orders for delete to authenticated using (public.is_super_admin());
-- Realtime
do $$ begin
 if not exists (
   select 1 from pg_publication_tables where pubname='supabase_realtime' and schemaname='public' and tablename='freight_orders'
 ) then alter publication supabase_realtime add table public.freight_orders; end if;
